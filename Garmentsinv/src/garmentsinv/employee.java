    private void txtecodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtecodeKeyPressed


if(evt.getKeyCode() == KeyEvent.VK_ENTER)
{

    try {
        String scode = txtecode.getText();
        pst = con.prepareStatement("select * from employee where employeeID = ?");
        pst.setString(1, scode);
        rs = pst.executeQuery();
        
        if(rs.next()== false)
        {
        JOptionPane.showMessageDialog(this,"Employee  not found");
        
        }
        else
        {
        
        String pname = rs.getString("employeename");
        txtename.setText(pname.trim());
      
        String price = rs.getString("wage");
        txtwage.setText(price.trim());
        
        txtatt.requestFocus();
        
        
        }
        
        
        
        // TODO add your handling code here:
    } catch (SQLException ex) {
        Logger.getLogger(employee.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_txtecodeKeyPressed
    }
