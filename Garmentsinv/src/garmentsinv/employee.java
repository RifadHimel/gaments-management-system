     df.addRow(new Object []{
     
     
     
         txtename.getText(),
         txtwage.getText(),
         txtatt.getValue().toString(),
         
      salary
     
     });
      int sum = 0;
       for(int i=0; i<jTable1.getRowCount();i++)
       {
       sum = sum + Integer.parseInt(jTable1.getValueAt(i,3).toString());
       
       
       
       }
    txtsalary.setText(String.valueOf(sum));
     txtecode.setText("");
     txtename.setText("");
     txtatt.setValue(0);
     txtwage.setText("");
     txtecode.requestFocus();
   
    

